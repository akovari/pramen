#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn builtins_include_core_sinks_and_source() {
        let ids: Vec<&str> = builtins().iter().map(|c| c.id).collect();
        assert!(ids.contains(&"sink.postgres"));
        assert!(ids.contains(&"sink.flightSql"));
        assert!(ids.contains(&"source.objectStore"));
        assert!(ids.contains(&"sink.adbc"));
        assert_eq!(builtin("sink.flightSql").unwrap().support_level, SupportLevel::Preview);
        assert_eq!(builtin("sink.adbc").unwrap().support_level, SupportLevel::Planned);
        assert!(builtin("nope").is_none());
    }

    #[test]
    fn ids_are_unique() {
        let mut seen = std::collections::BTreeSet::new();
        for connector in builtins() {
            assert!(seen.insert(connector.id), "duplicate id {}", connector.id);
        }
    }

    #[test]
    fn matrix_markdown_lists_every_builtin() {
        let md = matrix_markdown();
        for connector in builtins() {
            assert!(
                md.contains(&format!("`{}`", connector.id)),
                "matrix missing {}",
                connector.id
            );
        }
    }

    #[test]
    fn committed_matrix_artifact_is_current() {
        let committed = include_str!("../../../../docs/connectors/support-matrix.md");
        let expected = format!("# Connector support matrix\n\n{}", matrix_markdown());
        assert_eq!(
            committed, expected,
            "docs/connectors/support-matrix.md is stale; regenerate with \
             `cargo run -p pramen-core --example generate_connector_matrix --quiet \
              > docs/connectors/support-matrix.md`"
        );
    }
}
