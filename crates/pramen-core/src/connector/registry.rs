//! Built-in connector registry (E1.4).

use super::types::{
    ConnectorDescriptor, ConnectorKind, DeliveryContract, SupportLevel,
};

/// All first-party and planned connectors known to the lean binary / matrix.
#[must_use]
pub fn builtins() -> &'static [ConnectorDescriptor] {
    &BUILTINS
}

/// Look up a connector by stable id.
#[must_use]
pub fn builtin(id: &str) -> Option<&'static ConnectorDescriptor> {
    builtins().iter().find(|connector| connector.id == id)
}

/// Markdown body for `docs/connectors/support-matrix.md` (without the title).
///
/// Kept in code so the published matrix cannot drift from the registry.
#[must_use]
pub fn matrix_markdown() -> String {
    let mut out = String::from(
        "<!-- Generated by pramen_core::connector::matrix_markdown — do not edit rows by hand; update the registry. -->\n\
         \n\
         Support levels: **supported** (first-party, CI-backed), **preview** (shipped with\n\
         documented limits), **planned** (named, not in the default binary).\n\
         \n\
         | Id | Kind | Level | Modes / schemes | Delivery | Notes |\n\
         | --- | --- | --- | --- | --- | --- |\n",
    );
    for connector in builtins() {
        let modes_schemes = match connector.kind {
            ConnectorKind::Sink if !connector.modes.is_empty() => connector.modes.join(", "),
            ConnectorKind::Source if !connector.schemes.is_empty() => {
                connector.schemes.join(", ")
            }
            _ => "—".to_owned(),
        };
        out.push_str(&format!(
            "| `{}` | {} | {} | {} | {} | {} |\n",
            connector.id,
            connector.kind.as_str(),
            connector.support_level.as_str(),
            modes_schemes,
            connector.delivery.summary(),
            connector.notes,
        ));
    }
    out.push('\n');
    out
}

